#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*str;

	str = s;
	i = 0;
	if (!s)
		return ;
	while (i < n)
	{
		*str = 0;
		str++;
		i++;
	}
}
