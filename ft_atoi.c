#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	neg;
	int	multi;

	i = 0;
	multi = 0;
	neg = 1;
	if (!str[i])
		return (0);
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg *= -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		multi = (multi * 10) + (str[i] - 48);
		i++;
	}
	return (multi *= neg);
}
